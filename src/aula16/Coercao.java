//Testando os conceitos de coerção
public class Coercao {
	/*
	 * Coerção ocorre quando um tipo primitivo (ou objeto)
	 * é convertido em outro tipo primitivo ou objeto
	 * 
	 * Pode ser implícita ou explícita
	*/
	
	//Coerção explícita double -> float
	float w = (float) 12.4;
	
	//Coerção implícita int -> double
	double i = 1;
}
